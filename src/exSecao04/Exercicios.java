package exSecao04;

public class Exercicios {
	
	public double Somar(double n1, double n2) {
		return n1+n2;
	}
	
	public double AreaCirculo(double n1) {
		return 3.14159 * (n1*n1);
	}
	
	public double CalculaDiferenca(double n1,double n2,double n3,double n4) {
		return (n1 * n2 - n3 * n4);
	}
	
	public double CalculaSalario(double n1, double n2) {
		return n1*n2;
	}
	
	public double CalculaValoraPagar(int n1, double n2, int n3, double n4){
		return (n1*n2)+(n3*n4);
	}
	
	public double CalculaAreaTriangulo(double n1, double n2) {
		return (n1*n2)/2;
	}
	
	public double CalculaTrapezio(double n1, double n2, double n3) {
		return ((n1+n2)*n3)/2;
	}

	public double CalculaAreaQuadrado(double n1) {
		return n1*n1;
	}
	
	public double CalculaAreaRetangulo(double n1, double n2) {
		return n1*n2;
	}
}
