package exSecao04;

public class EstudoMath {

	public static void main(String[] args) {
		double a; 
		double b = 2;
		double c = -1;
		
		//Raiz Quadrada
		a = Math.sqrt(9);
		//Potencia
		b = Math.pow(a, b);
		//Valor absoluto
		c = Math.abs(c);
		
		System.out.println("Raiz Quadrada :" + a);
		System.out.println("Potência :" + b);
		System.out.println("Valor absoluto :" + c);
		
		
		

	}

}
